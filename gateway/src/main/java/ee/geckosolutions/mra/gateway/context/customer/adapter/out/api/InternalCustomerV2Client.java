package ee.geckosolutions.mra.gateway.context.customer.adapter.out.api;

import java.util.UUID;

import org.springframework.http.MediaType;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.service.annotation.GetExchange;
import org.springframework.web.service.annotation.HttpExchange;
import org.springframework.web.service.annotation.PostExchange;

@HttpExchange(url = "/internal/api/v2/customers")
public interface InternalCustomerV2Client {

    @GetExchange(url = "/{id}", accept = MediaType.APPLICATION_JSON_VALUE)
    ResponseEntity<byte[]> get(@PathVariable UUID id);

    @PostExchange(contentType = MediaType.APPLICATION_JSON_VALUE, accept = MediaType.APPLICATION_JSON_VALUE)
    ResponseEntity<byte[]> insert(@RequestBody byte[] content);

}
